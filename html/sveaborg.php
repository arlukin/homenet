          <h2>Eldinstruktioner - Köket</h2>
          <ul>
            <li>Öppna draget (spak med kula) på spisen, så att spjället (gallret) i nedre luckan är öppet.</li>
            <li>Öppna nedre luckan helt.</li>
            <li>Lägg in tidningspapper, ett ark, i övre luckan. Lägg lite bark ifrån vedlådan/vedboden ovanpå. Lägg in två vedträn ovanpå.</li>
            <li>Max tre åt gången någonsin, det kan bli för varmt annars.</li>
            <li>Tänd papperet på flera ställen.</li>
            <li>Vänta tills det börjar brinna.</li>
            <li>Stäng nedre luckan något, ungefär 1-2 centimeter öppet.</li>
            <li>Stäng övre luckan helt.</li>
            <li>Brinner det för mycket, stäng nedre luckan helt.</li>
            <li>Rycker det in, så har du troligen ställt spaken med kulan fel. Eller var det ett tag sedan det eldades i spisen. Då lägger du ett papper i facket (dragluckan) som är mellan övre och undre ugnen, öppna nedre ugnen för att finna facket. Tänd på papperet.</li>
            <li>Blir övrefacket blir fullt ta då rakan vid vedlådan, och skrapa ner askan genom gallret.</li>
            <li>När asklådan i det undre facket börjar bli full, vänta till morgondagen. Ta då ut lådan utomhus och töm den vid ”komposten” bakom vattenpumpen. (Bakom staketet)</li>
            <li>Häll sedan ett spann vatten över askan, kolla att det verkligen har slocknat. Kan ligga och glöda flera dagar annars.</li>
            <li>När du eldat klart och det brunnit klart, stänger du spjället, spak med kula.</li>
