          <h1><a name="Huset">Huset</a></h1>

          <h2>Veranda</h2>
          <p><img src="http://sveaborg.cybercow.se/images/stories/Sveaborg/HouseSmallPic/01%20ThePorchInside.JPG" border="0" alt="Den vackra verandan, här kan du koppla av och villa upp dig under de soliga sommardagarn på semestern." title="Den vackra verandan, här kan du koppla av och villa upp dig under de soliga sommardagarn på semestern." /></p>
          <p>En mysig veranda som gjord för uppesittarkvällar i sommarnatten. Äta en bit mat, dricka något gott. Umgås med varandra eller bara sitta och lyssna på naturen. </p>

          <h2>Uteplats</h2>
          <p>Inta eftermiddagskaffet på uteplatsen och blicka ut över den härligt gröna tomten. Kanske grilla något gott till kvällen.</p>

          <h2>Kök</h2>
          <p>Praktiskt ljust äldre kök med matplats för 4. En klassisk husqvarna vedspis som används både som värmekälla och till matlagning. Vanlig elspis finns också samt kylskåp med frysfack. Frysskåp finns i utrymme utanför badrumet.</p>

          <h2>Badrum</h2>
          <p>Rymligt badrum med wc, badkar samt tvättställ.</p>

          <h2>Matplats</h2>
          <p><img src="http://sveaborg.cybercow.se/images/stories/Sveaborg/HouseSmallPic/03%20Dining_room.JPG" border="0" alt="Sommarstugans matsal. Här kan du få lite omväxling i vardagen och njuta av en god måltid i den lantliga miljön." title="Sommarstugans matsal. Här kan du få lite omväxling i vardagen och njuta av en god måltid i den lantliga miljön." /> </p>
          <p>Husets finaste rum är den ombonade matsalen med en vacker kakelugn som värmer gott. Plats för minst 6 personer.</p>

          <h2>Sovrum 1</h2>
          <p>Sovrum 1, som vi i familjen kallar för rosa rummet, ligger på övervåningen. Här finns två enkelsängar som står på ett gammalt fint trägolv.</p>

          <h2>Sovrum 2</h2>
          <p>Sovrum 2 ligger på övervåningen. Här finns en dubbelsäng som står på ett gammalt fint trägolv. Angränsar till miniköket.</p>

          <h2>Sovrum 3 / TV-Rummet</h2>
          <p>Sovrum 3 ligger på bottenvåningen, och har en säng och en bäddsoffa med plats för två. Rummet används även som tv-rum.</p>

          <h2>Övre hall</h2>
          <p>Stor möblerad hall med vackert trägolv. Här finns utrymme för att lägga ut två extra madrasser.</p>

          <h2>Verkstaden</h2>
          <p>Utrymmet mellan sovrum 3 och badrummet kallar vi för verkstaden. Här hade gammelmorfar sin skomakarverkstad ibörjan av 1900-talet. I detta rum finns idag tillgång till tvättmaskin, torkställning och frys.</p>

          <h2>Minikök</h2>
          <p>På övervåningen finns ett gammalt minikök, med äldre vedspis (Får ej användas). Här bakade gammelmormor Augusta tårtor till hela byn.</p>

          <h2>Övriga utrymmen</h2>
          <p>
            <ul>
              <li>1 st klädkammare på undervåningen</li>
              <li>Mindre hall på undervåningen, som gränsar till sovrum 3, matsal och veranda.</li>
              <li>Vedbod.</li>
            </ul>
          </p>

