          <p>Tillbringa din semester i ett vackert sekelskifteshus beläget i Smålands djupa skogar med närhet till badsjö.</p>
          <p>Gå runt barfota på den härlig trädgårdstomten och låt gräset kittla dina fötter. Forsätt ut på upptäcksfärd bland de många mysiga stigar och grusvägar, och avsluta med ett svalkande dopp i Våmsjön.</p>
          <p>Slappna av tillsammans med vänner och familj på uteplatsen och grilla något gott. För att senare på kvällen flytta in i verandan och umgås till långt in på natten.</p>
          <p>Sveaborg ligger som första huset i en liten by ett par kilometer ifrån Mörlunda i Småland. Sommarhuset är inbäddat mellan hagar och skog högst upp på en liten backe, med öppet läge. Gångavstånd till badsjö i en lugn mijlö. Flera sällskapsutrymmen så som veranda, uteplats, matsal och tv-rum.</p>
          <p>Huset har 9 sovplatser utplacerade på runt 92 kvadratmeter. 3 enkelsängar, en dubbelsäng och en bädsoffa för två. Ytterliggare 2 extramadrasser finns tillgängliga att lägga ut i den övre hallen. Samt tillgång till en spjälsäng och barnstol. Mycket barnvänlig miljö. </p>
